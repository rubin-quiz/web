/**
 * 指定したジャンルからランダムに問題を選択
 * @param {string} genre 選択するジャンルの名前
 * @param {number} count 選択する問題数
 * @return {Array} 選択された問題の配列
 */
export function selectRandomQuestions(genre, count) {
    // 選択されたジャンルの問題を取得
    const genreQuestions = quizDataByGenre[genre];
    
    // 配列をコピーしてシャッフル
    const shuffled = [...genreQuestions].sort(() => 0.5 - Math.random());
    
    // 必要な数だけ問題を取得（問題数が足りない場合は全て返す）
    return shuffled.slice(0, Math.min(count, shuffled.length));
}