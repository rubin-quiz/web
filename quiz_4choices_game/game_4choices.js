// イベントリスナーの設定
function initGame() {
    // ジャンルボタンを動的に作成
    createGenreButtons();
    
    // モード選択ボタン
    DOM.buttons.mode5.addEventListener("click", () => startGame(5));
    DOM.buttons.mode10.addEventListener("click", () => startGame(10));
    
    // モード選択画面からジャンル選択に戻るボタン
    DOM.buttons.returnToGenre.fromMode.addEventListener("click", returnToGenreSelection);
    
    // 結果画面のボタン
    DOM.buttons.restart.addEventListener("click", restartGame);
    DOM.buttons.returnToGenre.fromResult.addEventListener("click", returnToGenreSelection);
    DOM.buttons.returnToMode.addEventListener("click", returnToModeSelection);
    
    // 初期表示設定
    DOM.screens.mode.style.display = "none";
    DOM.screens.quiz.style.display = "none";
    DOM.screens.result.style.display = "none";
}

// ジャンルボタンを動的に作成
function createGenreButtons() {
    const container = DOM.buttons.genreButtons;
    container.innerHTML = ""; // 既存のボタンをクリア
    
    Object.keys(quizDataByGenre).forEach(genre => {
        const button = document.createElement("button");
        button.textContent = genre;
        button.className = "mode-button";
        button.addEventListener("click", () => selectGenre(genre));
        container.appendChild(button);
    });
}

// ジャンル選択時の処理
function selectGenre(genre) {
    gameState.currentGenre = genre;
    DOM.screens.genre.style.display = "none";
    DOM.screens.mode.style.display = "block";
    DOM.quiz.genreTitle.textContent = genre; // モード選択画面のタイトルを更新
}

// ランダムに問題を選択
function selectRandomQuestions(count) {
    // 選択されたジャンルの問題を取得
    const genreQuestions = quizDataByGenre[gameState.currentGenre];
    
    // 配列をコピーしてシャッフル
    const shuffled = [...genreQuestions].sort(() => 0.5 - Math.random());
    
    // 必要な数だけ問題を取得
    return shuffled.slice(0, count);
}

// ゲーム開始
function startGame(questionCount) {
    DOM.screens.mode.style.display = "none";
    DOM.screens.quiz.style.display = "block";
    
    // 問題数を設定
    gameState.questionCount = questionCount;
    DOM.quiz.totalQuestions.textContent = questionCount;
    
    resetGameState();
    showQuestion();
}

// ゲーム状態のリセット
function resetGameState() {
    // ランダムに問題を選択
    gameState.selectedQuestions = selectRandomQuestions(gameState.questionCount);
    
    gameState.currentIndex = 0;
    gameState.score = 0;
    gameState.timeLeft = gameState.maxTime;
    gameState.userAnswers = [];
    gameState.answered = false;
    
    DOM.quiz.scoreDisplay.textContent = "0";
    DOM.quiz.currentQuestion.textContent = "1";
}

// 問題表示
function showQuestion() {
    // タイマーをクリア
    clearInterval(gameState.timer);
    cancelAnimationFrame(gameState.animationId);
    
    // 回答済みフラグをリセット
    gameState.answered = false;
    
    // タイマーを初期化
    gameState.timeLeft = gameState.maxTime;
    DOM.quiz.timeDisplay.textContent = gameState.timeLeft;
    
    // タイマーゲージを初期化
    initTimerGauge();

    // 現在の問題番号を更新
    DOM.quiz.currentQuestion.textContent = gameState.currentIndex + 1;

    // 問題と選択肢を表示
    const currentQuestion = gameState.selectedQuestions[gameState.currentIndex];
    DOM.quiz.question.textContent = currentQuestion.question;
    
    // 問題番号をdata属性として設定（これを追加）
    DOM.quiz.question.setAttribute('data-question-number', 'Q' + (gameState.currentIndex + 1));
    
    // アニメーション効果のクラスを追加
    DOM.quiz.question.classList.add('question-appear');
    
    // アニメーション終了後にクラスを削除（次回のために）
    setTimeout(() => {
        DOM.quiz.question.classList.remove('question-appear');
    }, 500);
    
    // 選択肢を表示
    renderChoices(currentQuestion);

    // タイマー開始
    startTimer();
}
// 選択肢をレンダリング
function renderChoices(questionData) {
    DOM.quiz.choices.innerHTML = "";
    
    // 選択肢と正解のインデックスをペアにして配列を作成
    const choicesWithCorrectIndex = questionData.choices.map((choice, index) => {
        return {
            text: choice,
            isCorrect: index === questionData.answer
        };
    });
    
    // 選択肢をシャッフル
    const shuffledChoices = [...choicesWithCorrectIndex].sort(() => 0.5 - Math.random());
    
    // シャッフルした選択肢を表示
    shuffledChoices.forEach((choiceObj, index) => {
        const button = document.createElement("button");
        button.textContent = choiceObj.text;
        button.classList.add("game-button");
        button.setAttribute("data-index", String.fromCharCode(65 + index)); // A, B, C, Dのようにアルファベットで表示
        
        // 正解かどうかの情報を使って回答チェック
        button.onclick = () => checkAnswerByCorrectFlag(choiceObj.isCorrect, button);
        
        DOM.quiz.choices.appendChild(button);
    });
}

// タイマーゲージの初期化
function initTimerGauge() {
    // 既存のタイマーゲージを削除
    const existingContainer = document.querySelector(".timer-container");
    if (existingContainer) {
        existingContainer.remove();
    }
    
    // 新しいタイマーゲージを作成
    const timerContainer = document.createElement("div");
    timerContainer.classList.add("timer-container");
    
    const timerGauge = document.createElement("div");
    timerGauge.classList.add("timer-gauge");
    timerGauge.style.width = "100%";
    
    timerContainer.appendChild(timerGauge);
    
    // タイマー表示の前に挿入
    const timerDisplay = document.getElementById("game-timer");
    timerDisplay.parentNode.insertBefore(timerContainer, timerDisplay);
    
    // DOMオブジェクトに格納
    DOM.quiz.timerContainer = timerContainer;
}

// 正解フラグによる回答チェック
function checkAnswerByCorrectFlag(isCorrect, selectedButton) {
    // 既に回答済みの場合は処理しない
    if (gameState.answered) return;
    
    // 回答済みにする
    gameState.answered = true;
    
    clearInterval(gameState.timer);
    cancelAnimationFrame(gameState.animationId); // アニメーションをキャンセル
    
    const currentQuestion = gameState.selectedQuestions[gameState.currentIndex];
    const buttons = document.querySelectorAll("#game-choices-container .game-button");
    
    // ユーザーの回答を記録
    gameState.userAnswers.push({
        question: currentQuestion.question,
        choices: [...buttons].map(b => b.textContent), // 表示されている選択肢（シャッフル後）
        correctAnswer: currentQuestion.choices[currentQuestion.answer],
        userAnswer: selectedButton ? selectedButton.textContent : null
    });

    // 選択肢の色を変更
    buttons.forEach((button) => {
        if (button.textContent === currentQuestion.choices[currentQuestion.answer]) {
            button.classList.add("game-correct");
        } else if (button === selectedButton && !isCorrect) {
            button.classList.add("game-wrong");
        }
        button.disabled = true;
    });

    // 正解なら得点を加算
    if (isCorrect) {
        gameState.score++;
        DOM.quiz.scoreDisplay.textContent = gameState.score;
        showCorrectFeedback();
    }

    setTimeout(nextQuestion, 1500);
}

// タイマー開始
function startTimer() {
    const startTime = Date.now();
    const duration = gameState.maxTime * 1000; // ミリ秒に変換
    
    // 最初のゲージ更新（遅延なしで即時反映）
    updateTimerGauge(100);
    
    // 1秒ごとの表示更新用タイマー
    gameState.timer = setInterval(() => {
        gameState.timeLeft--;
        DOM.quiz.timeDisplay.textContent = gameState.timeLeft;
        
        if (gameState.timeLeft <= 0) {
            clearInterval(gameState.timer);
            cancelAnimationFrame(gameState.animationId);
            timeOut();
        }
    }, 1000);
    
    // アニメーションフレームを使った滑らかなゲージ更新
    function animate() {
        const elapsedTime = Date.now() - startTime;
        const remainingPercentage = Math.max(0, 100 - (elapsedTime / duration * 100));
        
        updateTimerGauge(remainingPercentage);
        
        if (remainingPercentage > 0 && gameState.timeLeft > 0 && !gameState.answered) {
            gameState.animationId = requestAnimationFrame(animate);
        }
    }
    
    gameState.animationId = requestAnimationFrame(animate);
}

// 時間切れ処理
function timeOut() {
    if (gameState.answered) return;
    
    gameState.answered = true;
    
    const currentQuestion = gameState.selectedQuestions[gameState.currentIndex];
    const buttons = document.querySelectorAll("#game-choices-container .game-button");
    
    // ユーザーの回答を記録
    gameState.userAnswers.push({
        question: currentQuestion.question,
        choices: [...buttons].map(b => b.textContent), // 表示されている選択肢（シャッフル後）
        correctAnswer: currentQuestion.choices[currentQuestion.answer],
        userAnswer: null // 時間切れ
    });
    
    // 正解の選択肢を強調表示
    buttons.forEach((button) => {
        if (button.textContent === currentQuestion.choices[currentQuestion.answer]) {
            button.classList.add("game-correct");
        }
        button.disabled = true;
    });
    
    setTimeout(nextQuestion, 1500);
}

// タイマーゲージの更新
function updateTimerGauge(percentage) {
    const gauge = document.querySelector(".timer-gauge");
    if (!gauge) return;
    
    // 渡されたパーセンテージでゲージ幅を設定
    gauge.style.width = `${percentage}%`;
    
    // 30%以下で赤色に変更
    if (percentage <= 30) {
        gauge.classList.add("timer-critical");
    } else {
        gauge.classList.remove("timer-critical");
    }
}

// 正解エフェクト
function showCorrectFeedback() {
    const feedback = document.createElement("div");
    feedback.classList.add("correct-feedback");
    feedback.textContent = "正解！";
    document.body.appendChild(feedback);
    
    setTimeout(() => {
        feedback.remove();
    }, 1000);
}

// 次の問題へ
function nextQuestion() {
    gameState.currentIndex++;
    if (gameState.currentIndex < gameState.questionCount) {
        showQuestion();
    } else {
        showResult();
    }
}

// 結果画面表示
function showResult() {
    DOM.screens.quiz.style.display = "none";
    DOM.screens.result.style.display = "block";
    
    // スコア表示を整形
    const scoreText = `${gameState.score}/${gameState.questionCount}`;
    DOM.result.finalScore.textContent = `Score: ${scoreText}`;
    
    // 合格/不合格の判定
    const passPercent = 80; // 80%以上で合格
    const userPercent = (gameState.score / gameState.questionCount) * 100;
    
    let resultMessage;
    if (userPercent >= passPercent) {
        resultMessage = "合格！おめでとうございます！";
        DOM.result.finalScore.innerHTML += `<br><span style="color:var(--correct-color);font-size:1.2rem;">${resultMessage}</span>`;
    } else {
        resultMessage = "不合格です！もう一度チャレンジしてみよう！";
        DOM.result.finalScore.innerHTML += `<br><span style="color:var(--wrong-color);font-size:1.2rem;">${resultMessage}</span>`;
    }
    
    // X共有ボタンの追加
    addTwitterSharingButton(gameState.score, gameState.questionCount, resultMessage);
    
    createResultTable();
}

